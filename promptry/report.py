def render_compare_report(data: dict) -> str:
    """Render model comparison as a self-contained HTML page.

    Expects a dict shaped like a serialised ``ModelCompareReport``:
        suite_name, baseline {model_version, run_count, overall_mean, ...},
        candidate {model_version, run_count, overall_mean, ...},
        overall_delta, percentile, assertion_comparisons [...],
        cost_ratio, score_per_dollar_baseline, score_per_dollar_candidate,
        verdict, verdict_reason.
    """
    suite_name = data.get("suite_name", "unknown")
    baseline = data.get("baseline", {})
    candidate = data.get("candidate", {})
    verdict = data.get("verdict", "comparable")
    verdict_reason = data.get("verdict_reason", "")
    overall_delta = data.get("overall_delta", 0.0)
    percentile = data.get("percentile", 50.0)
    assertion_comparisons = data.get("assertion_comparisons", [])
    cost_ratio = data.get("cost_ratio")
    spd_baseline = data.get("score_per_dollar_baseline")
    spd_candidate = data.get("score_per_dollar_candidate")

    b_model = baseline.get("model_version", "?")
    b_runs = baseline.get("run_count", 0)
    b_mean = baseline.get("overall_mean", 0.0)
    b_std = baseline.get("overall_std", 0.0)
    b_min = baseline.get("overall_min", 0.0)
    b_max = baseline.get("overall_max", 0.0)
    b_cost = baseline.get("avg_cost_per_call", 0.0)

    c_model = candidate.get("model_version", "?")
    c_runs = candidate.get("run_count", 0)
    c_mean = candidate.get("overall_mean", 0.0)
    c_cost = candidate.get("avg_cost_per_call", 0.0)

    verdict_upper = verdict.upper().replace("_", " ")
    if verdict == "switch":
        v_badge = "badge-switch"
    elif verdict == "keep_baseline":
        v_badge = "badge-keep"
    else:
        v_badge = "badge-comparable"

    parts: list[str] = []

    # Header
    parts.append(
        "<h1>Model Comparison Report</h1>\n"
        "<p class=\"subtitle\">Suite: <strong>{suite}</strong></p>".format(
            suite=_esc(suite_name),
        )
    )

    # Summary card
    parts.append(
        "<div class=\"card\">\n"
        "  <table>\n"
        "    <tr><th></th><th>{b_model} (baseline)</th><th>{c_model} (candidate)</th></tr>\n"
        "    <tr><th>Runs</th><td>{b_runs}</td><td>{c_runs}</td></tr>\n"
        "    <tr><th>Mean Score</th>"
        "<td class=\"score\">{b_mean:.3f} &plusmn; {b_std:.3f}</td>"
        "<td class=\"score\">{c_mean:.3f}</td></tr>\n"
        "    <tr><th>Range</th>"
        "<td class=\"muted\">{b_min:.3f} &mdash; {b_max:.3f}</td>"
        "<td class=\"muted\">&Delta; {delta:+.3f} ({pctl:.0f}th pctl)</td></tr>\n"
        "  </table>\n"
        "</div>".format(
            b_model=_esc(b_model),
            c_model=_esc(c_model),
            b_runs=b_runs,
            c_runs=c_runs,
            b_mean=b_mean,
            b_std=b_std,
            c_mean=c_mean,
            b_min=b_min,
            b_max=b_max,
            delta=overall_delta,
            pctl=percentile,
        )
    )

    # Per-assertion breakdown
    if assertion_comparisons:
        parts.append("<h2>Per-Assertion Comparison</h2>")
        parts.append(
            "<div class=\"card\">\n"
            "  <table>\n"
            "    <tr><th>Assertion</th><th>Baseline</th><th>Candidate</th>"
            "<th>Delta</th><th>Verdict</th></tr>"
        )
        for ac in assertion_comparisons:
            a_type = ac.get("assertion_type", "?")
            a_b_mean = ac.get("baseline_mean", 0.0)
            a_b_std = ac.get("baseline_std", 0.0)
            a_c_score = ac.get("candidate_score", 0.0)
            a_delta = ac.get("delta", 0.0)
            a_verdict = ac.get("verdict", "comparable")

            if a_verdict == "better":
                v_cls = "pass"
            elif a_verdict == "worse":
                v_cls = "fail"
            else:
                v_cls = "muted"

            parts.append(
                "    <tr>"
                "<td>{atype}</td>"
                "<td class=\"score\">{b_mean:.3f} &plusmn; {b_std:.3f}</td>"
                "<td class=\"score\">{c_score:.3f}</td>"
                "<td>{delta:+.3f}</td>"
                "<td class=\"{v_cls}\">{verdict}</td>"
                "</tr>".format(
                    atype=_esc(a_type),
                    b_mean=a_b_mean,
                    b_std=a_b_std,
                    c_score=a_c_score,
                    delta=a_delta,
                    v_cls=v_cls,
                    verdict=a_verdict,
                )
            )
        parts.append("  </table>\n</div>")

    # Cost analysis
    if cost_ratio is not None:
        pct = abs(1 - cost_ratio) * 100
        direction = "cheaper" if cost_ratio < 1 else "more expensive"

        parts.append("<h2>Cost Analysis</h2>")
        parts.append(
            "<div class=\"card\">\n"
            "  <table>\n"
            "    <tr><th></th><th>{b_model}</th><th>{c_model}</th></tr>\n"
            "    <tr><th>Cost / call</th>"
            "<td>${b_cost:.4f}</td><td>${c_cost:.4f}</td></tr>\n"
            "    <tr><th>Relative</th>"
            "<td colspan=\"2\">Candidate is {pct:.0f}% {direction}</td></tr>".format(
                b_model=_esc(b_model),
                c_model=_esc(c_model),
                b_cost=b_cost,
                c_cost=c_cost,
                pct=pct,
                direction=direction,
            )
        )
        if spd_baseline is not None and spd_candidate is not None:
            parts.append(
                "    <tr><th>Score / $</th>"
                "<td>{spd_b:.0f}</td><td>{spd_c:.0f}</td></tr>".format(
                    spd_b=spd_baseline,
                    spd_c=spd_candidate,
                )
            )
        parts.append("  </table>\n</div>")

    # Verdict
    parts.append(
        "<div class=\"verdict-box\">\n"
        "  <h2 style=\"margin-top:0\">Verdict: "
        "<span class=\"badge {v_badge}\">{verdict}</span></h2>\n"
        "  <p>{reason}</p>\n"
        "</div>".format(
            v_badge=v_badge,
            verdict=_esc(verdict_upper),
            reason=_esc(verdict_reason),
        )
    )

    return _wrap_html(
        "promptry - {} comparison".format(suite_name),
        "\n".join(parts),
    )

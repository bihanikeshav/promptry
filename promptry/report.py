def _esc(value: object) -> str:
    """HTML-escape any value."""
    return html.escape(str(value))


def _timestamp() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")


def _wrap_html(title: str, body: str) -> str:
    return (
        "<!DOCTYPE html>\n"
        "<html lang=\"en\">\n"
        "<head>\n"
        "  <meta charset=\"utf-8\">\n"
        "  <meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">\n"
        "  <title>{title}</title>\n"
        "  <style>{css}</style>\n"
        "</head>\n"
        "<body>\n"
        "<div class=\"container\">\n"
        "{body}\n"
        "<div class=\"footer\">Generated by promptry &middot; {ts}</div>\n"
        "</div>\n"
        "</body>\n"
        "</html>"
    ).format(title=_esc(title), css=_BASE_CSS, body=body, ts=_timestamp())


# ---------------------------------------------------------------------------
# Run report
# ---------------------------------------------------------------------------

def render_run_report(results: dict) -> str:
    """Render eval run results as a self-contained HTML page.

    Expects a dict shaped like a serialised ``SuiteResult``:
        suite_name, overall_pass, overall_score, tests (list of dicts with
        test_name, passed, latency_ms, error, assertions [...]).
    """
    suite_name = results.get("suite_name", "unknown")
    overall_pass = results.get("overall_pass", False)
    overall_score = results.get("overall_score", 0.0)
    tests = results.get("tests", [])

    pass_class = "pass" if overall_pass else "fail"
    pass_label = "PASS" if overall_pass else "FAIL"
    badge_class = "badge-pass" if overall_pass else "badge-fail"

    # Header
    parts: list[str] = []
    parts.append(
        "<h1>Eval Run Report</h1>\n"
        "<p class=\"subtitle\">Suite: <strong>{suite}</strong></p>".format(
            suite=_esc(suite_name),
        )
    )

    # Summary card
    parts.append(
        "<div class=\"card\">\n"
        "  <table>\n"
        "    <tr><th>Suite</th><td>{suite}</td></tr>\n"
        "    <tr><th>Result</th><td><span class=\"badge {badge}\">{label}</span></td></tr>\n"
        "    <tr><th>Score</th><td class=\"score\">{score:.3f}</td></tr>\n"
        "    <tr><th>Tests</th><td>{n_tests}</td></tr>\n"
        "  </table>\n"
        "</div>".format(
            suite=_esc(suite_name),
            badge=badge_class,
            label=pass_label,
            score=overall_score,
            n_tests=len(tests),
        )
    )

    # Per-test results
    if tests:
        parts.append("<h2>Test Results</h2>")
        for test in tests:
            t_name = test.get("test_name", "?")
            t_pass = test.get("passed", False)
            t_latency = test.get("latency_ms", 0.0)
            t_error = test.get("error")
            assertions = test.get("assertions", [])

            t_status_class = "pass" if t_pass else "fail"
            t_status_label = "PASS" if t_pass else "FAIL"

            parts.append(
                "<div class=\"card\">\n"
                "  <h2 style=\"margin-top:0\">"
                "<span class=\"{cls}\">{label}</span> {name}"
                " <span class=\"muted\" style=\"font-weight:normal; font-size:0.85rem\">"
                "({latency:.0f}ms)</span></h2>".format(
                    cls=t_status_class,
                    label=t_status_label,
                    name=_esc(t_name),
                    latency=t_latency,
                )
            )

            if t_error:
                parts.append(
                    "  <p class=\"fail\" style=\"margin-bottom:0.75rem\">{err}</p>".format(
                        err=_esc(t_error),
                    )
                )

            if assertions:
                parts.append(
                    "  <table>\n"
                    "    <tr><th>Assertion</th><th>Score</th><th>Status</th></tr>"
                )
                for a in assertions:
                    a_type = a.get("assertion_type", "?")
                    a_score = a.get("score")
                    a_passed = a.get("passed", False)
                    score_str = "{:.3f}".format(a_score) if a_score is not None else "-"
                    a_cls = "pass" if a_passed else "fail"
                    a_label = "ok" if a_passed else "FAIL"
                    parts.append(
                        "    <tr>"
                        "<td>{atype}</td>"
                        "<td class=\"score\">{score}</td>"
                        "<td class=\"{cls}\">{label}</td>"
                        "</tr>".format(
                            atype=_esc(a_type),
                            score=score_str,
                            cls=a_cls,
                            label=a_label,
                        )
                    )
                parts.append("  </table>")

            parts.append("</div>")

    return _wrap_html("promptry - {} run report".format(suite_name), "\n".join(parts))


